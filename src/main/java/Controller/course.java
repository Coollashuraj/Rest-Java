package Controller;

public class course {
    private String name;
    private int count;

    public course(String name, int count) {
        this.name = name;
        this.count = count;
    }

    public String getName(String name) {
        return name;
    }


    public void setName() {

        this.name =name;
}

    public int getCount(Integer count) {
        return  count;
    }


    public void setCount() {

        this.count =count;
    }

}
